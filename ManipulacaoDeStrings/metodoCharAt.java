package ManipulacaoDeStrings;

public class metodoCharAt {
    public static void main(String[] args) {

        //Em Java, você acessa um caractere pelo índice usando o metodo charAt(int index). O primeiro caractere está no índice 0.

        String texto = "Python";
        char primeiroCaractere = texto.charAt(0);
        System.out.println(primeiroCaractere);
    }
}
